#import "UIColor+DesignHandoff.h"

@implementation UIColor (DesignHandoff)

+ (UIColor *)customOrangeColor {
    return [UIColor colorWithRed:1.0f green:0.67f blue:0.0f alpha:0.5f];
}

+ (UIColor *)customRedColor {
    return [UIColor colorWithRed:1.0f green:0.2f blue:0.2f alpha:1.0f];
}

+ (UIColor *)customGreenColor {
    return [UIColor colorWithRed:0.0f green:0.4f blue:0.0f alpha:1.0f];
}


@end
